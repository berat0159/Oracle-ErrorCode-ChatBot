package enties;

public class Answer {

	private int answID;
	private String answTittle;
	private String answCode;
	private int queID;
	public int getAnswID() {
		return answID;
	}
	public void setAnswID(int answID) {
		this.answID = answID;
	}
	public String getAnswTittle() {
		return answTittle;
	}
	public void setAnswTittle(String answTittle) {
		this.answTittle = answTittle;
	}
	public String getAnswCode() {
		return answCode;
	}
	public void setAnswCode(String answCode) {
		this.answCode = answCode;
	}
	public int getQueID() {
		return queID;
	}
	public void setQueID(int queID) {
		this.queID = queID;
	}
}
